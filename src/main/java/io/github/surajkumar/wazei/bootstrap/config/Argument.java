package io.github.surajkumar.wazei.bootstrap.config;

public class Argument {
    private String type;
    private String name;

    public Argument() {}

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}